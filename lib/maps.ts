// You can create additional maps here
export const createEmptyMap = (width: number, height: number): number[][] => {
  const map: number[][] = []

  for (let y = 0; y < height; y++) {
    map[y] = []
    for (let x = 0; x < width; x++) {
      // Create walls around the edges
      if (y === 0 || y === height - 1 || x === 0 || x === width - 1) {
        map[y][x] = 3 // Wall
      } else {
        map[y][x] = 0 // Empty
      }
    }
  }

  return map
}

// Helper function to add dots to a map
export const addDotsToMap = (map: number[][]): number[][] => {
  const newMap = JSON.parse(JSON.stringify(map))

  for (let y = 0; y < newMap.length; y++) {
    for (let x = 0; x < newMap[y].length; x++) {
      if (newMap[y][x] === 0) {
        newMap[y][x] = 1 // Add dot
      }
    }
  }

  return newMap
}

// Helper function to add power pellets to a map
export const addPowerPelletsToMap = (map: number[][], positions: { x: number; y: number }[]): number[][] => {
  const newMap = JSON.parse(JSON.stringify(map))

  positions.forEach((pos) => {
    if (newMap[pos.y] && newMap[pos.y][pos.x] !== 3) {
      newMap[pos.y][pos.x] = 2 // Add power pellet
    }
  })

  return newMap
}
